#Programa que gera a letra da canção muito popular entre os programadores: 99 bugs no software, pegue onze deles e conserte... Tecle "CTRL+F5" 88 bugs no software, pegue onze deles e conserte... Sem erros no software! Está estabilizado!

def cancao():
    for i in range(99, 0, -11):
        print(f"{i} bugs no software, pegue onze deles e conserte...")
        print(f'Tecle "Ctrl+F5"')

    print(f"Sem erros no software! Está estabilizado!")
        
cancao()